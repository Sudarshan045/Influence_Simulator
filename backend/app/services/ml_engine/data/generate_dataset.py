# ── build dataframe ───────────────────────────────────────────────────────────

rows = []
for idea_name, (waypoints, noise_scale, m_base, e_base, sentiment_mean) in CATALOGUE.items():
    trend   = make_curve(waypoints, noise_scale=noise_scale)
    ments, engs = social_signals(trend, m_base, e_base)
    sents   = sentiment_curve(sentiment_mean)

    for i in range(n):
        rows.append({
            "date":       dates[i],
            "idea":       idea_name,
            "trend_score": round(float(trend[i]), 2),
            "mentions":   int(ments[i]),
            "engagement": int(engs[i]),
            "sentiment":  float(sents[i]),
        })

df = pd.DataFrame(rows)
df = df.sort_values(["idea", "date"]).reset_index(drop=True)

out_path = os.path.join(os.path.dirname(__file__), "ideas_dataset.csv")
df.to_csv(out_path, index=False)

