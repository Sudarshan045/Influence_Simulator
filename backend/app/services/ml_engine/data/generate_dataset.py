# ── helpers ──────────────────────────────────────────────────────────────────

def make_curve(waypoints, noise_scale=4):
    """Interpolate (month_idx, value) waypoints then add realistic noise."""
    xs = [w[0] for w in waypoints]
    ys = [w[1] for w in waypoints]
    curve = np.interp(np.arange(n), xs, ys)
    return np.clip(curve + np.random.normal(0, noise_scale, n), 5, 100)


def social_signals(trend, mentions_base=1000, engagement_base=400):
    m = (trend / 100) * mentions_base * np.random.uniform(0.7, 1.4, n)
    e = (trend / 100) * engagement_base * np.random.uniform(0.6, 1.5, n)
    return np.round(m).astype(int), np.round(e).astype(int)


def sentiment_curve(mean, std=0.12):
    return np.clip(np.random.normal(mean, std, n), -1, 1).round(3)


